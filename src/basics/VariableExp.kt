package basics

fun main() {
//    Muttable variable
    var name = "Gowtham"
    name = "Ashok"
    println("My name is $name")

//    immutable variable
    val lname = "Kamal"
    println("My name is $lname")

//    Declaration first and initialization later
    var fName: String
    fName = "GOWTHAM Ashok"
    println("My name is $fName")


}